ll remove_duplicate(vector<ll>&ar, ll len){
	if(len<=1){
		return len;
	}
	ll last_idx=1;
	for(ll i=1; i<len; i++){
		if(ar[i]!=ar[i-1]){
			ar[last_idx++]=ar[i];
		}
	}
	return last_idx;
}


